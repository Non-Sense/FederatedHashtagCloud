export default class WordCloudElement {
    c: number;
    x: number;
    y: number;
    e: number;
    s: number;
    l: string;
    t: string;

    constructor(c: number, x: number, y: number, e: number, s: number, l: string, t: string) {
        this.c = c;
        this.x = x;
        this.y = y;
        this.e = e;
        this.s = s;
        this.l = l;
        this.t = t;
    }
}